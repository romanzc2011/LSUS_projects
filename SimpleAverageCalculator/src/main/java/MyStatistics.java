public class MyStatistics {
    
    //==========================================================================
    //==========================================================================
    // ==== CONSTRUCTOR
    //==========================================================================
    //==========================================================================
    public MyStatistics() {
        
        
    }
    
    
    //==========================================================================
    //==========================================================================
    // ==== PUBLIC METHODS
    //==========================================================================
    //==========================================================================

    /**
     * Get average
     * @param v1 Value 1
     * @param v2 Value 2
     * @param v3 Value 3
     * @param v4 Value 4
     * @param v5 Value 5
     * @return The average of five values
     */
    
    public static double getAverage(int v1, int v2, int v3, int v4, int v5) {
        return getSum(v1, v2, v3, v4, v5) / 5;
        
    }
    
    
    //==========================================================================
    //==========================================================================
    // ==== PRIVATE METHODS
    //==========================================================================
    //==========================================================================
    private static double getSum(int v1, int v2, int v3, int v4, int v5) {
        return v1 + v2 + v3 + v4 + v5;
        
    }
    

}
