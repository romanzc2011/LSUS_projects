package staticvariablesmethods;

/**
 *
 * @author mtrutsch
 */
public class MyMath {
  // ===============================================================================================
  // ===============================================================================================
  // ===== CONSTRUCTOR
  // ===============================================================================================
  // ===============================================================================================
  public MyMath() {
  }

  
  // ===============================================================================================
  // ===============================================================================================
  // ===== PUBLIC METHODS
  // ===============================================================================================
  // ===============================================================================================

  /**
   * Calculates a sum of two integers
   * @param a Value 1
   * @param b Value 2
   * @return The sum of two integers
   */
  public static double add(double a, double b) {
    return a + b;
  }
}








